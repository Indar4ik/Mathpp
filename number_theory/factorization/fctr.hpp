inline void sqrt_fctr(uint64_t n, std::vector<uint64_t>& factors) noexcept {
    if (n <= 1) return;
    factors.clear();
    factors.reserve(64);

    int twos = std::countr_zero(n);
    n >>= twos;
    for (int i = 0; i < twos; ++i) factors.push_back(2);
    while (n % 3 == 0) { factors.push_back(3); n /= 3; }
    while (n % 5 == 0) { factors.push_back(5); n /= 5; }
    while (n % 7 == 0) { factors.push_back(7); n /= 7; }
    while (n % 11 == 0) { factors.push_back(11); n /= 11; }
    while (n % 13 == 0) { factors.push_back(13); n /= 13; }

    uint64_t i = 17;
    uint64_t idx = 0ull;

    while (i * i <= n){
        while (true){
            uint64_t m = n;
            uint64_t q = n / i;
            uint64_t r = n % i;
            n = r ? m : q;
            if (r != 0) break;
            factors.push_back(i);
        }
        i += WHEEL[idx];
        if (++idx == WHEEL_SIZE) idx = 0ull;
    }
    if (n > 1) factors.push_back(n);
}

