constexpr uint64_t SQ_MASK = []{
    uint64_t m = 0ull;
    for (uint64_t i = 0; i < 64; ++i) m |= 1ull << ((i * i) & 63ull);
    return m;
}();

inline bool is_square(uint64_t c, uint64_t& root) noexcept {
    uint64_t r = static_cast<uint64_t>(std::sqrt(static_cast<double>(c)));
    // double может ошибиться на ±1
    if (r * r > c) --r;
    if ((r + 1) * (r + 1) <= c) ++r;
    root = r;
    return r * r == c;
}

// Pollard p-1: ищет нетривиальный делитель нечётного составного n.
// Идея (малая теорема Ферма): если (p-1) | M, то p | a^M - 1, значит p | gcd(a^M - 1, n).
// M строим по простым q <= B, для каждого берём q^(макс степень <= B) -> один pow на q.
// Возвращает делитель в (1, n) или 0, если p-1 не B-гладкое (тогда падаем в ferma).
inline uint64_t pollard_pm1(uint64_t n, uint32_t B) noexcept {
    [[assume((n & 1ull) == 1ull)]];
    // Решето простых до B строим один раз и переиспользуем между вызовами.
    static uint32_t cached_B = 0;
    static std::vector<uint32_t> primes;
    if (B > cached_B) { primes = sieve(B); cached_B = B; }

    mont M(n);
    uint64_t a = M.to(2);                       // основание a = 2 в Montgomery-форме
    for (uint32_t q : primes) {
        if (q > B) break;                       // кэш может быть шире текущего B
        // e = наибольшая степень q, не превосходящая B
        uint64_t e = q;
        while (e * q <= B) e *= q;
        a = M.pow(a, e);                        // накапливаем q^e в показателе за один pow
        uint64_t g = std::gcd(M.from(a) - 1, n); // a-1 в обычном виде; gcd с n
        if (g == n) return 0;                   // перелёт: меньший B или другой a; пас
        if (g > 1) return g;                    // нашли нетривиальный делитель
    }
    return 0;                                   // p-1 не B-гладкое
}

inline void ferma_fctr(uint64_t n, std::vector<uint64_t> &factors) noexcept {
    if (n <= 1ull) return;
    if (is_prime(n)) { factors.push_back(n); return; }

    // Сначала Pollard p-1: закрывает несбалансированные делители (где Ферма буксует).
    // Делитель g может быть составным -> рекурсивно раскладываем обе части.
    if (uint64_t g = pollard_pm1(n, 1000000)) {
        ferma_fctr(g, factors);
        ferma_fctr(n / g, factors);
        return;
    }

    // p-1 пасанул (p-1 не гладкое) -> Ферма добивает сбалансированный случай.
    // Граница: a*a не должно переполнить uint64_t, т.е. a <= floor(sqrt(2^64 - 1)).
    constexpr uint64_t A_MAX = 4294967295ull;   // 2^32 - 1
    uint64_t r;
    uint64_t a = static_cast<uint64_t>(std::ceil(std::sqrt(n)));
    uint64_t c = a * a - n;
    while (a <= A_MAX){
        if (SQ_MASK >> (c & 63ull) & 1ull) [[unlikely]] {
            if (is_square(c, r)){
                ferma_fctr(a - r, factors);
                ferma_fctr(a + r, factors);
                return;
            }
        }
        c += (a++ << 1) + 1;
    }
    // Ферма не сошёлся до границы (несбалансированное n с негладким p-1).
    // Честный отказ: кладём нераскрытый остаток как есть. Произведение факторов
    // остаётся == n, но этот множитель составной. Полностью закрывает только Pollard rho.
    factors.push_back(n);
}

